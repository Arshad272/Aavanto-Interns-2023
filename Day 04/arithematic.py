# They are used for performing arithematic operations
# output will be int/float

# +, -, *, /, %, //, **

# / - returns float after performing division
# // (floor division) - returns int after performing division
# % (modulus) - return reminder after performing division
# ** - it is used to calculate power of 2 numbers

a = 5
b = 2
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)
print(4 ** 3)